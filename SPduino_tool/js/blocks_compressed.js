Blockly.Blocks.main={
	init:function() {
		this.appendDummyInput()
			.appendField("%{BKY_INITIAL_SETUP}");
		this.appendStatementInput("SETUP")
		this.appendDummyInput()
			.appendField("%{BKY_INITIAL_LOOP}");
		this.appendStatementInput("LOOP")
		this.setInputsInline(!1);
		this.setStyle("initializes_blocks");
	},onchange: function(event) {
		if (!this.workspace) {
		  // Block is deleted.
		  return;
		}
		if (!event.recordUndo) {
		  // Events not generated by user. Skip handling.
		  return;
		}
		
		if (showCode) {
			var code = Blockly.Arduino.workspaceToCode();			
			editor.setValue(code);	
		}			
		
		var blocks = this.workspace.getBlocksByType("main");
		if (blocks.length>1) {
			for (var i=1;i<blocks.length;i++) {
				blocks[i].dispose();
			}
		}
		var enabledBlockList = ["main"];
		var variableBlockList = ["variables_set","variables_set1","variables_set7"];
		var variableGlobalBlockList = ["variables_set","variables_set1"];
		blocks = this.workspace.getAllBlocks();
		var p;
		for (var i=0;i<blocks.length;i++) {
			p = blocks[i];
			if (enabledBlockList.includes(p.type)||variableBlockList.includes(p.type)||(p.previousConnection==null&&p.outputConnection
==null)) {
				if (topCheck) blocks[i].setEnabled(true);
				if (variableGlobalBlockList.includes(blocks[i].type)&&blocks[i].getField("POSITION")) {
					if (blocks[i].getFieldValue("POSITION")=="global")
						continue;
				}
				else
					continue;
			}
			p = p.getParent()||p.getPreviousBlock()?p.getParent()||p.getPreviousBlock():"";
			while(p) {
				if ((enabledBlockList.includes(p.type)||variableBlockList.includes(p.type)||(p.previousConnection==null&&p.outputConnection
==null))&&!p.getParent()) {
					if (topCheck) blocks[i].setEnabled(true);
					break;
				}
				p = p.getParent()||p.getPreviousBlock()?p.getParent()||p.getPreviousBlock():"";
			}
			if (!p) {
				if (topCheck) blocks[i].setEnabled(false);
			}
			if (blocks[i].getParent()&&blocks[i].getPreviousBlock()) {
				if (variableBlockList.includes(p.type)&&variableBlockList.includes(blocks[i].getParent().type)) {
					if (topCheck) blocks[i].unplug();
				}
			}
		}
	}
};

